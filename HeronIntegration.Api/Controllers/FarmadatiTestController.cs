    [HttpGet("")]
    public async Task<IActionResult> GetBaseInfo()
    {
        var code = "033262027";
        var result = await _provider.GetBaseInfoAsync(code);
        var description = await _longProvider.GetLongDescriptionAsync(code);
        var img = await _imgProvider.GetImagesAsync(code);

        if (result == null)
            return NotFound($"Prodotto {code} non trovato");

        return Ok(img);
    }

    public class ProductMongo
    {
        [JsonPropertyName("ProductName")]
        public string ProductName { get; set; }

        [JsonPropertyName("CustomerId")]
        public string CustomerId { get; set; }

        [JsonPropertyName("Aic")]
        public string Aic { get; set; }
    }


    [HttpGet("bulk-static")]
    public async Task<IActionResult> GetProductsStatic()
    {
        var aicList = AicList();

        var semaphore = new SemaphoreSlim(10); // limita parallelismo
        var tasks = aicList.Select(async code =>
        {
            await semaphore.WaitAsync();

            try
            {
                var baseInfo = await _provider.GetBaseInfoAsync(code);

                return new ProductMongo
                {
                    ProductName = baseInfo == null
                        ? "NOT_FOUND"
                        : baseInfo.Name ?? "UNKNOWN",

                    CustomerId = "69bab7ebd24d96d52d5c8dc9",
                    Aic = code
                };
            }
            catch (Exception ex)
            {
                return new ProductMongo
                {
                    ProductName = "ERROR",
                    CustomerId = "69bab7ebd24d96d52d5c8dc9",
                    Aic = code
                };
            }
            finally
            {
                semaphore.Release();
            }
        });
        var results = (await Task.WhenAll(tasks)).ToList();

        var json = JsonSerializer.Serialize(results, new JsonSerializerOptions
        {
            WriteIndented = true
        });

        System.IO.File.WriteAllText("products.json", json);

        return Ok(results);
    }


